// Endpoint pour obtenir tous les articles de blog
app.get('/blog/posts', (req, res) => {
  try {
    const { page = 1, limit = 6, category } = req.query;
    const db = readBlogDatabase();
    let posts = db.blog_posts;
    
    // Filtrer par cat√©gorie si sp√©cifi√©e
    if (category) {
      posts = posts.filter(post => post.category.toLowerCase() === category.toLowerCase());
    }
    
    // Pagination
    const startIndex = (page - 1) * limit;
    const endIndex = startIndex + parseInt(limit);
    const paginatedPosts = posts.slice(startIndex, endIndex);
    
    res.json({
      posts: paginatedPosts,
      total: posts.length,
      page: parseInt(page),
      totalPages: Math.ceil(posts.length / limit),
      hasNext: endIndex < posts.length,
      hasPrev: page > 1
    });
  } catch (error) {
    console.error('‚ùå Erreur r√©cup√©ration articles blog:', error);
    res.status(500).json({ error: 'Erreur lors de la r√©cup√©ration des articles' });
  }
});

